// Function to open a specific gallery
function openGallery(galleryId) {
    document.getElementById(galleryId).style.display = "block";
}

// Function to close a specific gallery
function closeGallery(galleryId) {
    document.getElementById(galleryId).style.display = "none";
}

function openFood(foodId) {
    document.getElementById(foodId).style.display = "block";
}

// Function to close a specific gallery
function closeFood(foodId) {
    document.getElementById(foodId).style.display = "none";
}